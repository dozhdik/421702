#pragma once

#include <iostream>
#include <map>
#include <algorithm>

/**
 * @class Ribbon
 * @brief Класс, реализующий ленту для модели Поста (сдвиг и метки).
 * @details Лента хранит метки (значения) в ассоциативном контейнере по индексам
 * и поддерживает перемещение текущей позиции, чтение/запись и очистку.
 */
class Ribbon{
private:

    /** 
     * @brief позиция головки на ленте. 
     */
    int position;  

    /** 
     * @brief контейнер, отвечающий за хранение значений ленты. 
     */   
    std::map<int, int> ribbon;

    /**
     * @brief Устанавливает значение в указанной позиции ленты.
     * @param userPosition Индекс ячейки ленты, в которую записывается значение.
     * @param userValue Значение для записи (исключительно 0/1).
     */
    void setValue(int userPosition, int userValue);
public:

    /**
     * @brief Конструктор по умолчанию.
     * @details Инициализирует ленту пустой (без меток) и позицию в ноль.
     */
    Ribbon();

    /**
     * @brief Устанавливает метку по текущей позиции (внешний API).
     * @details Записать 1 в текущую ячейку ленты.
     */
    void setLabel();

    /**
     * @brief Удаляет метку по текущей позиции (если была).
     */
    void removeLabel();

    /**
     * @brief Сдвигает головку ленты вправо (увеличивает `position`).
     */
    void moveRight();

    /**
     * @brief Сдвигает головку ленты влево (уменьшает `position`).
     */
    void moveLeft();

    /**
     * @brief Возвращает значение в текущей позиции ленты.
     * @return Значение в текущей ячейке (если метки нет, обычно 0 или другой дефолт).
     */
    int getValue() const;

    /**
     * @brief Очищает все метки на ленте и сбрасывает позицию в начальное состояние.
     */
    void clear();

    /**
     * @brief Оператор вывода ленты в поток.
     * @param os Поток вывода.
     * @param rb Объект ленты для печати.
     * @return Ссылка на поток `os` для цепочки вызовов.
     */
    friend std::ostream& operator<<(std::ostream& os, const Ribbon& rb);
};